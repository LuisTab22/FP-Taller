#Permita mostrar las cuatro opreaciones básicas de dos números

print("Halle la suma, resta multiplicación y división de los siguientes números")

#Asignamos los datos
numero1 = int(input("Número 1: "))
numero2 = int(input("Número 2: "))

resultado = numero1 + numero2
print("Suma", resultado)

resultado = numero1 - numero2
print("Resta", resultado)

resultado = numero1 * numero2
print("Multiplicación", resultado)

resultado = numero1 / numero2
print("División", resultado)
