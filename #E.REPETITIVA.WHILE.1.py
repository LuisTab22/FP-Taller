#Imprimir los números del 1 al 10 con WHILE

numero = 1 #Definimos el valor incial como 1
while numero <= 10: #Indicamos cuando la operación llega a su fin
    print(numero) #1 < 10, verdadero, por consiguiente 
    numero = numero + 1 #El uno es IMPRESO y se ejecuta esta linea, (numero = 1 + 1), el nuevo valor de NUMERO es 2, por tanto se vuelve a ejecutar el WHILE
print("Fin")
#Al final, cuando el WHILE resulte falso, se detendra el programa, dejando impreso los numeros que si cumplieron con el WHILE
